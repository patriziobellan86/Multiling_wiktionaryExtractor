#* ''Et quand je m'approchai du '''fauteuil''' de grand’mère pour lui baiser la main, elle se détourna et cacha sa main sous son mantelet.'' {{source|{{w|Léon Tolstoï}}, ''Souvenirs'', 1851-1857, traduction de Ardève Barine, éd.1922}}
#* ''Les clients […] s'installaient placidement, qui sur des banquettes de velours moelleux, qui dans le creux de '''fauteuils''' hospitaliers.'' {{source|{{Citation/Victor Méric/Les Compagnons de l’Escopette/1930|78}}}}
#* ''Il gagna la grande salle de réception aux '''fauteuils''' surmontés de dorures.'' {{source|{{w|Out-el-Kouloub}}, ''Zaheira'', dans &quot;Trois contes de l'Amour et de la Mort&quot;, 1940}}
